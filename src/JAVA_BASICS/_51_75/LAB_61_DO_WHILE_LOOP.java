package JAVA_BASICS._51_75;

public class LAB_61_DO_WHILE_LOOP {
    public static void main(String[] args) {

        int a = 10;
        do {
            System.out.println(a);
            a++;
        }while (a<10);

        int b = 10;
        while (b <10){
            System.out.println(b);
            b++;
        }
    }
}
