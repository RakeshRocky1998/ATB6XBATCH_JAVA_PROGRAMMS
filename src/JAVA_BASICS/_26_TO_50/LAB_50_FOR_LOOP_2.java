package JAVA_BASICS._26_TO_50;

public class LAB_50_FOR_LOOP_2 {
    public static void main(String[] args) {
        for (int i = 1; i < 12; i++) {
            System.out.println(i);
            if (i == 11){
                break;

            }
            System.out.println(i);
        }
    }
}