package JAVA_BASICS._51_TO_75;

public class LAB_66_ARRAY_FOR_LOOP {
    public static void main(String[] args) {
        int[] a = {4,5,6,7,8};

        for (int i = 0; i < a.length; i++) {
            System.out.println(a[i]);

        }
        for (int i = 1; i <= a.length-1; i++) {
            System.out.println(a[i]);

        }
    }
}
