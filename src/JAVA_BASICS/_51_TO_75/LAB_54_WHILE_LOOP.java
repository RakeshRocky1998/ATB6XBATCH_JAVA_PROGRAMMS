package JAVA_BASICS._51_TO_75;

public class LAB_54_WHILE_LOOP {
    public static void main(String[] args) {
        //while
        // A - Init
        // B - Condition
        // C - Incre

        int i = 1; // A
        while (i<=10){ // B
            // Execute the code
            System.out.println(i);
            i++; // C

            // i - 1, 10 , print 1 to 10
        }

        for (int j = 0; j <=10 ; j=j+2) {
            System.out.println(j);
        }

    }
}
